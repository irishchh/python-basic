marks = int(input("Enter your total marks"))

if marks >=90:
    print("Excellent A+")
elif marks <=80:
    print("A")
elif marks <=70:
    print("B+")
elif marks <=60:
    print("B")
elif marks <=50:
    print("C+")
elif marks <=40:
    print("C")
elif marks <=30:
    print("D")
else:
    print("Non Grade")